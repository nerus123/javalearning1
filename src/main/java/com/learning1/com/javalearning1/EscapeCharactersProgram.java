package com.learning1.com.javalearning1;

public class EscapeCharactersProgram {

	public static void main(String[] args) {
     //\t inserts tab
	System.out.println("We are learning \tautomation ");
	 //\b inserts backspace
	System.out.println("We are learning \bautomation ");
	 //\n inserts newline
	System.out.println("We are learning \nautomation ");
	// \r insets carriage return
	System.out.println("We are learning \rautomation ");
	// \f inserts form feed at a point
	System.out.println("We are learning \fautomation ");
	// \' inserts single quote
	System.out.println("We are learning \'automation\' ");
	// \\"inserts double quote
	System.out.println("We are learning \"automation\" ");
	// \\inserts  backslash
	System.out.println("We are learning \\automation ");
	

	}

}
